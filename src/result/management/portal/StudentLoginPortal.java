    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String rollNumber = jTextField1.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/srmp","root","Vamshi@123");
            Statement st1 = conn.createStatement();
            ResultSet rsFromResult = st1.executeQuery("Select * from result where rollnumber = '"+rollNumber+"'");
            Statement st2 = conn.createStatement();
            ResultSet rsFromStudent = st2.executeQuery("Select * from student where rollnumber ='"+rollNumber+"'");
            if(rsFromResult.next()){
                setVisible(false);
                new StudentResultDisplay(rollNumber).setVisible(true);
            }
            else if(rsFromStudent.next()){
                JOptionPane.showMessageDialog(null,"Your result still not updated by Teacher..Please wait till it get updated");
                setVisible(false);
                new Home().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Please check your RollNumber");
                setVisible(false);
                new StudentLoginPortal().setVisible(true);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
            setVisible(false);
            new StudentLoginPortal().setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

