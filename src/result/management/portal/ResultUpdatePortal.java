    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollNumber = jTextField1.getText();
        String maths = jTextField2.getText();
        String physics = jTextField3.getText();
        String dsa = jTextField4.getText();
        String workShop = jTextField5.getText();
        String project = jTextField6.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/srmp","root","Vamshi@123");
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("Select * from student where rollnumber = '"+rollNumber+"' ");
            if(rs.next()){
              st.executeUpdate("insert into result(rollnumber,maths,physics,dsa,workshop,project) values('"+rollNumber+"','"+maths+"','"+physics+"','"+dsa+"','"+workShop+"','"+project+"')");
              JOptionPane.showMessageDialog(null,"Result saved");
              setVisible(false);
              new ResultUpdatePortal().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Not saved....Enter student details before entering result");
                setVisible(false);
                new ResultUpdatePortal().setVisible(true);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e+"Some error occurred and result not saved");
            setVisible(false);
            new ResultUpdatePortal().setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

